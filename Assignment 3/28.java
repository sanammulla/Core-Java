/**
   28.	Initialize one String type of array and print the elements using for each loop.
*/

 class ArrayString{
     public static void main(String[] args) {
         char arr[] = new char[]{'S','U','M','I','T','N','L','A','B'};

         for(char a : arr){
             System.out.print(" "+a);
         }
     }
 }